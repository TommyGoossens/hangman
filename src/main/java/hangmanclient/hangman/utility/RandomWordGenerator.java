    /**
     * checks if the word is completely guessed
     * @param guessedCorrectLetters array of letters the user currently has correct
     * @return true or false depending on the result
     */
    @Override
    public boolean wordIsCompletelyGuessed(List<String> guessedCorrectLetters) {
        for (int i = 0; i < secretWord.length(); i++) {
            if (!Character.toString(secretWord.charAt(i)).equals(guessedCorrectLetters.get(i))) {
                return false;
            }
        }
        return true;
    }

    /**
     * Generates a new word for single player mode
     */
    @Override
    public void generateNewWord() {

        int index;
        switch (difficulty) {
            case EASY:
                index = random.nextInt(easyWords.size());
                this.secretWord = easyWords.get(index);
                break;
            case MEDIUM:
                index = random.nextInt(mediumWords.size());
                this.secretWord = mediumWords.get(index);
                break;
            case HARD:
                index = random.nextInt(hardWords.size());
                this.secretWord = hardWords.get(index);
                break;
        }
        this.secretWord = this.secretWord.toUpperCase();
    }

